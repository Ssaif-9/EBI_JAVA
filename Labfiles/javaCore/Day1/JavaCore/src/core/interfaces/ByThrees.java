package core.interfaces;

public class ByThrees implements Numbers,Constants{
	int start;
	int val;
	String s;

	public ByThrees() {
		start = 0;
		val = 0;
	}

	public int getNext() {
		val += 3;
		return val;
	}

	public void reset() {
		val = start;
	}

	public void setStart(int x) {
		start = x;
		val = x;
		s = mon;
		
	}
}
